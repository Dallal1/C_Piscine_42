#include <unistd.h>

void	ft_is_negative(int n)
{
	char	name_p;
	char	name_n;

	name_p = 'P';
	name_n = 'N';
	if (n >= 0)
	{
		write (1, &name_p, 1);
	}
	else
	{
		write (1, &name_n, 1);
	}
}
